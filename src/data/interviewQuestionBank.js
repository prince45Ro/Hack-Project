function buildBestAnswer(scenario, theme, variant) {
  return [
    variant.intro,
    `I would start by ${theme.assess}.`,
    `Then I would ${theme.action}.`,
    `I would stay closely aligned with ${theme.collaboration} so the solution reflects ${scenario.focus}.`,
    `The outcome I would highlight is ${theme.result}.`,
    scenario.close,
  ].join(" ");
}

export const INTERVIEW_QUESTION_BANK = INTERVIEW_SCENARIOS.flatMap((scenario) =>
  scenario.themes.flatMap((theme, themeIndex) =>
    PROMPT_VARIANTS.map((variant, variantIndex) => ({
      id: `${scenario.key}-${String(themeIndex + 1).padStart(2, "0")}-${variant.key}`,
      scenarioKey: scenario.key,
      scenarioLabel: scenario.label,
      question: variant.buildQuestion(theme, scenario),
      bestAnswer: buildBestAnswer(scenario, theme, variant),
      framework: variant.framework,
      difficulty: theme.difficulty,
      interviewerFocus: scenario.rubric,
      topic: theme.topic,
      tags: [scenario.label, variant.label, theme.difficulty],
      sortOrder: themeIndex * PROMPT_VARIANTS.length + variantIndex,
    }))
  )
);

export const QUESTION_BANK_SIZE = INTERVIEW_QUESTION_BANK.length;

export function inferInterviewScenario(interview = {}) {
  const combinedText = [interview.title, interview.category, interview.description]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();

  if (!combinedText.trim()) {
    return "general";
  }

  let bestScenario = INTERVIEW_SCENARIOS[0];
  let bestScore = -1;

  INTERVIEW_SCENARIOS.forEach((scenario) => {
    const score = scenario.keywords.reduce(
      (total, keyword) => total + (combinedText.includes(keyword.toLowerCase()) ? 1 : 0),
      0
    );

    if (score > bestScore) {
      bestScenario = scenario;
      bestScore = score;
    }
  });

  return bestScore > 0 ? bestScenario.key : "general";
}
